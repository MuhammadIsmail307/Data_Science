# Text Data = String 
# Examples :
a="I can do whatever I want"
b="This is my 6th class of Python Programming"
print(a)
print(b)

# Boolean Data : True or False
# Examples :
a1=("True")
b1=("False")
print(a1)
print(b1)

# Numeric Data : Integer, Float, Complex
# Examples :
a2=(1)
b2=(2.5)
c2=(3 , 5)
print(a2)
print(b2)
print(c2)

# Sequence Data : list, Tuple
# Examples :

list = [22, 26, 307, ["orange", "banana", "tarbooz"]]
print(list)

tuple = (("sher", "daish", ), ("positivity", "negativity"))
print(tuple)

# Mapped Data : Dict
# Examples :

dict1 = {"name":"ismail", "age":18, "passion": "life"}
print(dict1)